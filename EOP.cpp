/****************************************************************************************

  Determine the index corresponding to the given time

  dfJD: time in JD of interest
  dfJDUTC: UTC time in JD of interest
  bJDUTC: true, then the given dfJD is in UTC, otherwise dfJD is in ET

  nIndex: determined index

*****************************************************************************************/

bool cEOP::GetIndex( double dfJD, double &dfJDUTC, bool bJDUTC, int &nIndex )
{
	if( !m_bInit )
	{
		return false;
	}

	dfJDUTC = dfJD;

	if( !bJDUTC ) ConvertETUTC( dfJD, dfJDUTC, true );

	nIndex = -1;

	int nStart = m_nLastIndex - 1500;
	nStart = max( nStart, 0 );

	for( int i = nStart; i < m_nNumberEOP - 1; i++ )
	{
		if( dfJDUTC >= m_pstEOP[ i ].dfJD && dfJDUTC < m_pstEOP[ i + 1 ].dfJD )
		{
			nIndex = i;
			break;
		}
	}

	if( nIndex == -1 ) return false;

	if( nIndex > 0 ) nIndex--;
	if( m_nNumberEOP - nIndex < 4 ) nIndex = m_nNumberEOP - 4;

	m_nLastIndex = nIndex;

	return true;
}

