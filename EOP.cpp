/****************************************************************************************

  Read EOP parameters from a unified EOP data file and TAI-UTC difference from the given
  file

  szEOPFileName: unified EOP data file. This file is generated by the application
                 EOPUnify in c:\Projects\EOPUnify. Its default name should be
				 D:\EOSORDP\IERS_EOP\ComEOP.Dat.
  szTAImUTCFileName: the file contains the difference between UTC and TAI, its default
                     file name is D:\EOSORDP\IERS_EOP\tai-utc.dat

*****************************************************************************************/

bool cEOP::ReadModified( const char *szEOPFileName, const char *szTAImUTCFileName )
{
	if( m_bInit ) return true;	// the files have been read

	// check whether the file exists
	if (!fs::exists(szEOPFileName)) return false;
	if (!fs::exists(szTAImUTCFileName)) return false;

	m_bInit = false;

	if( !ReadTAIUTCData( szTAImUTCFileName ) )
	{
		return false;
	}

	m_nNumberEOP = 0;
	m_pstEOP.clear();

	EOPData tEOP;

	{
		std::string line, sub;

        std::ifstream in( szEOPFileName );
        if ( !in.is_open( ) )
        {
            return false;
        }

		while( getline( in, line ) )
		{
			sub = line.substr( 0, 12 );
			tEOP.dfJD = atof( sub.c_str() );

			sub = line.substr( 12, 11 );
			tEOP.dfMJD = atof( sub.c_str() );

			sub = line.substr( 23, 13 );
			tEOP.dfX = atof( sub.c_str() ) / 206264.8;

			sub = line.substr( 36, 13 );
			tEOP.dfY = atof( sub.c_str() ) / 206264.8;

			sub = line.substr( 49, 13 );
			tEOP.dfUT1UTC = atof( sub.c_str() );

			sub = line.substr( 75, 13 );
			tEOP.dfDPSI = atof( sub.c_str() ) / 206264.8;

			sub = line.substr( 88, 13 );
			tEOP.dfDEPS = atof( sub.c_str() ) / 206264.8;

			m_pstEOP.push_back( tEOP );
			m_nNumberEOP++;
		}

		m_bInit = true;
		m_nLastIndex = 0;

		return true;
	}
}

bool cEOP::Read( const char *szEOPFileName, const char *szTAImUTCFileName )
{
	if( m_bInit ) return true;	// the files have been read

	// check whether the file exists
	if (!fs::exists(szEOPFileName)) return false;
	if (!fs::exists(szTAImUTCFileName)) return false;

	m_bInit = false;

	if( !ReadTAIUTCData( szTAImUTCFileName ) )
	{
		return false;
	}

	/////////////////////////////////
	// read EOP file

	m_nNumberEOP = 0;
	m_pstEOP.clear();


	std::ifstream in( szEOPFileName );
	if ( !in.is_open( ) )
		return false;
	
	std::string line;

	/////////////////////////////////
	// Observed

	// get OBSERVED number
	int observed_num{0};
	while(std::getline(in, line))
	{
		auto pos = line.find("NUM_OBSERVED_POINTS");
		if (pos != std::string::npos)
		{
			observed_num = std::atoi(line.substr(pos + 20).c_str());
			break;
		}
	}

	if (observed_num == 0)
		return false;
	
	std::getline( in, line );
	if (line.find("BEGIN OBSERVED") == std::string::npos)
		return false;

	for (size_t i = 0; i < observed_num; i++)
	{
		EOPData eop;
		in >> eop;
		m_pstEOP.emplace_back(std::move(eop));
	}

	std::getline( in, line );
	if (line.find("END OBSERVED") == std::string::npos)
		return false;
	
	/////////////////////////////////
	// PREDICTED

	int predicted_num{0};
	while(std::getline(in, line))
	{
		auto pos = line.find("NUM_PREDICTED_POINTS");
		if (pos != std::string::npos)
		{
			predicted_num = std::atoi(line.substr(pos + 21).c_str());
			break;
		}
	}

	if (predicted_num == 0)
		return false;
	
	std::getline( in, line );
	if (line.find("BEGIN PREDICTED") == std::string::npos)
		return false;

	for (size_t i = 0; i < predicted_num; i++)
	{
		EOPData eop;
		in >> eop;
		m_pstEOP.emplace_back(std::move(eop));
	}

	std::getline( in, line );
	if (line.find("END PREDICTED") == std::string::npos)
		return false;

	m_nNumberEOP =  observed_num + predicted_num;

	m_bInit = true;
	m_nLastIndex = 0;

	return true;
}

/****************************************************************************************

  Compute the polar trans matrix at the given time.
  The trans matrix is from mean pole to true pole.

  Here, dfJD is regarded as UTC JD, the same as in the EOP data file. Because the
  differences between UTC, UT, TAI, ET are small enough to be neglected for the purpose
  of computing the polar motion matrix, dfJD could be in any one of the UTC, UT, TAL and
  ET time systems.

*****************************************************************************************/

bool cEOP::ComputePole( double dfJD, double *pdfPole )
{
	if( !m_bInit )
	{
		return false;
	}

	double x, y;

	if( !GetPolarMotion( dfJD, x, y, true ) )
	{
		return false;
	}

	pdfPole[ 0 ] = 1.0;
	pdfPole[ 3 ] = x * y;
	pdfPole[ 6 ] = x;
	pdfPole[ 1 ] = 0.0;
	pdfPole[ 4 ] = 1.0;
	pdfPole[ 7 ] = -y;
	pdfPole[ 2 ] = -x;
	pdfPole[ 5 ] = y;
	pdfPole[ 8 ] = 1.0;

	return true;
}

/****************************************************************************************

  Get the polar motion parameters at the given time

  dfJD: time in JD of interest
  dfX, dfY: obtained polar motion parameters in radians
  bJDUTC: true, then the given dfJD is in UTC, otherwise dfJD is in ET

*****************************************************************************************/

bool cEOP::GetPolarMotion( double dfJD, double &dfX, double &dfY, bool bJDUTC, bool bApplyDiurnalCorrection )
{
	if( !m_bInit )
	{
		return false;
	}

	int nIndex;
	double dfJDUTC;

	if( !GetIndex( dfJD, dfJDUTC, bJDUTC, nIndex ) )
	{
		return false;
	}

	dfX = dfY = 0;
	double dfTX, dfTY;

	for( int i = nIndex; i < nIndex + 4; i++ )
	{
		dfTX = m_pstEOP[ i ].dfX;
		dfTY = m_pstEOP[ i ].dfY;

		for( int j = nIndex; j < nIndex + 4; j++ )
		{
			if( i != j )
			{
				double dfT = ( dfJDUTC - m_pstEOP[ j ].dfJD ) /
					         ( m_pstEOP[ i ].dfJD - m_pstEOP[ j ].dfJD );

				dfTX *= dfT;
				dfTY *= dfT;
			}
		}

		dfX += dfTX;
		dfY += dfTY;
	}

	if( !bApplyDiurnalCorrection ) return true;

	// correct for diurnal and semi-diurnal tidal variation
	double eop[ 3 ];

	ComputeTidalVariation( dfJD, eop[ 0 ], eop[ 1 ], eop[ 2 ] );

	dfX += eop[ 0 ] / 206264.8;
	dfY += eop[ 1 ] / 206264.8;

	return true;
}

bool cEOP::GetPolarMotion( int nIndex, double &dfJD, double &dfX, double &dfY, bool bApplyDiurnalCorrection )
{
	if( !m_bInit )
	{
		return false;
	}

	if( nIndex < 0 || nIndex >= m_nNumberEOP )
	{
		return false;
	}

	dfJD = m_pstEOP[ nIndex ].dfJD;
	dfX = m_pstEOP[ nIndex ].dfX;
	dfY = m_pstEOP[ nIndex ].dfY;

	if( !bApplyDiurnalCorrection ) return true;

	// correct for diurnal and semi-diurnal tidal variation
	double eop[ 3 ];

	ComputeTidalVariation( dfJD, eop[ 0 ], eop[ 1 ], eop[ 2 ] );

	dfX += eop[ 0 ] / 206264.8;
	dfY += eop[ 1 ] / 206264.8;

	return true;
}

/****************************************************************************************

  Get the UT1-UTC parameter at the given epoch

  dfJD: time in JD of interest
  dfUT1UTC: obtained UT1-UTC parameters in seconds of time
  bJDUTC: true, then the given dfJD is in UTC, otherwise dfJD is in ET

  The leap second problem is treated. When a leap second is introduced, the UT1-UTC
  values will have a jump of 1 second. Because we use 4 UT1-UTC table values to determine
  the UT1-UTC at the epoch of interest, so the standatd deviation of the four table values
  (against their mean) is used to see whether there is a leap second within the four
  table values.

*****************************************************************************************/

bool cEOP::GetUT1UTC( double dfJD, double &dfUT1UTC, bool bJDUTC, bool bApplyDiurnalCorrection )
{
	dfUT1UTC = 0;
	if( !m_bInit )
	{
		return false;
	}

	int nIndex;
	double dfJDUTC;

	if( !GetIndexUT1UTC( dfJD, dfJDUTC, bJDUTC, nIndex ) )
	{
		return false;
	}

	int nIndex0 = nIndex;

	if( nIndex0 > 0 ) nIndex0--;

	double p[ 4 ], sum = 0;
	int k = 0, i;

	for( i = nIndex0; i < nIndex0 + 4; i++ )
	{
		p[ k ] = m_pstEOP[ i ].dfUT1UTC;
		sum += p[ k ];
		k++;
	}

	double mean = sum / 4.0;

	sum = 0;

	for( i = 0; i < 4; i++ ) sum += ( p[ i ] - mean ) * ( p[ i ] - mean );

	double std = sqrt( sum / 4.0 );

	if( std > 0.1 ) // there is a leap second
	{
		if( fabs( p[ 0 ] - p[ 1 ] ) > 0.1 )
		{
			if( fabs( p[ 1 ] - p[ 0 ] - 1.0 ) < 0.01 ) p[ 0 ] += 1.0;
			else if( fabs( p[ 1 ] - p[ 0 ] + 1.0 ) < 0.01 ) p[ 0 ] -= 1.0;
		}

		for( i = 2; i < 4; i++ )
		{
			if( fabs( p[ i ] - p[ 1 ] ) > 0.1 )
			{
				if( fabs( p[ i ] - p[ 1 ] - 1.0 ) < 0.01 ) p[ i ] -= 1.0;
				else if( fabs( p[ i ] - p[ 1 ] + 1.0 ) < 0.01 ) p[ i ] -= 1.0;
			}
		}
	}

	dfUT1UTC = 0;
	double dfTX;

	k = 0;

	for( i = nIndex0; i < nIndex0 + 4; i++ )
	{
		dfTX = p[ k ];

		for( int j = nIndex0; j < nIndex0 + 4; j++ )
		{
			if( i != j )
			{
				double dfT = ( dfJDUTC - m_pstEOP[ j ].dfJD ) /
					         ( m_pstEOP[ i ].dfJD - m_pstEOP[ j ].dfJD );

				dfTX *= dfT;
			}
		}

		dfUT1UTC += dfTX;
		k++;
	}

	if( !bApplyDiurnalCorrection ) return true;

	// correct for diurnal and semi-diurnal tidal variation
	double eop[ 3 ];

	ComputeTidalVariation( dfJD, eop[ 0 ], eop[ 1 ], eop[ 2 ] );

	dfUT1UTC += eop[ 2 ];

	return true;
}

/****************************************************************************************

  Get the TAI and UTC difference at given epoch

  dfJD: time in JD of interest
  dfTAIUTC: obtained TAI-UTC differece in seconds of time
  bJDUTC: true, then the given dfJD is in UTC, otherwise dfJD is in TAI

*****************************************************************************************/

bool cEOP::GetTAIUTC( double dfJD, double &dfTAIUTC, bool bJDUTC )
{
	if( !m_bInit )
	{
		return false;
	}

	if( bJDUTC )
	{
		if( dfJD < m_pstTAIUTC[ 0 ].dfJDUTC )
		{
			return false;
		}
	}
	else
	{
		if( dfJD < m_pstTAIUTC[ 0 ].dfJDTAI )
		{
			return false;
		}
	}


	int nIndex = -1;

	for( int i = 0; i < m_nNumberTAIUTC - 1; i++ )
	{
		if( bJDUTC )
		{
			if( dfJD >= m_pstTAIUTC[ i ].dfJDUTC &&
				dfJD < m_pstTAIUTC[ i + 1 ].dfJDUTC )
			{
				nIndex = i;
				break;
			}
		}
		else
		{
			if( dfJD >= m_pstTAIUTC[ i ].dfJDTAI &&
				dfJD < m_pstTAIUTC[ i + 1 ].dfJDTAI )
			{
				nIndex = i;
				break;
			}
		}
	}

	if( nIndex == -1 ) nIndex = m_nNumberTAIUTC - 1;

	double dfJDUTC = dfJD;

	if( !bJDUTC ) dfJDUTC = dfJD - m_pstTAIUTC[ nIndex ].df0 / 86400.0;

	dfTAIUTC = m_pstTAIUTC[ nIndex ].df0 +
		       m_pstTAIUTC[ nIndex ].df1 *
			   ( dfJDUTC - g_dfMJDRef - m_pstTAIUTC[ nIndex ].dfMJD );

	return true;
}


