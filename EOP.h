extern class cEOP g_EOP;

struct EOPData
{
	double dfJD;			// in UTC
	double dfMJD;			// MJD corresponding to dfJD
	double dfX;				// polar motion, in radian
	double dfY;				// polar motion, in radian
	double dfUT1UTC;		// in sec
	double dfDPSI;			// in radian
	double dfDEPS;			// in radian

	friend std::ifstream& operator>>(std::ifstream& ifs, EOPData &eop)
	{
		int year{0}, month{0}, day{0};
		double lod{0}, dx{0}, dy{0};
		std::string line;

		std::getline(ifs, line);
		std::stringstream ss(line);

		ss >> year >> month >> day >> eop.dfMJD >> eop.dfX >> eop.dfY >>
			eop.dfUT1UTC >> lod >> eop.dfDPSI >> eop.dfDEPS;

		eop.dfJD = eop.dfMJD + g_dfMJDRef;
		eop.dfX 	*= g_dfSEC2RAD;
		eop.dfY 	*= g_dfSEC2RAD;
		eop.dfDPSI 	*= g_dfSEC2RAD;
		eop.dfDEPS 	*= g_dfSEC2RAD;

		return ifs;
	}
};

